package codesignal

import (
	"github.com/stretchr/testify/assert"
	"testing"
)

func TestSudoku(t *testing.T) {
	assert.Equal(t, true, sudoku([][]string{
		{".", ".", ".", "1", "4", ".", ".", "2", "."},
		{".", ".", "6", ".", ".", ".", ".", ".", "."},
		{".", ".", ".", ".", ".", ".", ".", ".", "."},
		{".", ".", "1", ".", ".", ".", ".", ".", "."},
		{".", "6", "7", ".", ".", ".", ".", ".", "9"},
		{".", ".", ".", ".", ".", ".", "8", "1", "."},
		{".", "3", ".", ".", ".", ".", ".", ".", "6"},
		{".", ".", ".", ".", ".", "7", ".", ".", "."},
		{".", ".", ".", "5", ".", ".", ".", "7", "."},
	}))

	assert.Equal(t, false, sudoku([][]string{
		{".", ".", ".", ".", "2", ".", ".", "9", "."},
		{".", ".", ".", ".", "6", ".", ".", ".", "."},
		{"7", "1", ".", ".", "7", "5", ".", ".", "."},
		{".", "7", ".", ".", ".", ".", ".", ".", "."},
		{".", ".", ".", ".", "8", "3", ".", ".", "."},
		{".", ".", "8", ".", ".", "7", ".", "6", "."},
		{".", ".", ".", ".", ".", "2", ".", ".", "."},
		{".", "1", ".", "2", ".", ".", ".", ".", "."},
		{".", "2", ".", ".", "3", ".", ".", ".", "."},
	}))

	assert.Equal(t, true, sudoku([][]string{
		{".", ".", ".", "1", "4", ".", ".", "2", "."},
		{".", ".", "6", ".", ".", ".", ".", ".", "."},
		{".", ".", ".", ".", ".", ".", ".", ".", "."},
		{".", ".", "1", ".", ".", ".", ".", ".", "."},
		{".", "6", "7", ".", ".", ".", ".", ".", "9"},
		{".", ".", ".", ".", ".", ".", "8", "1", "."},
		{".", "3", ".", ".", ".", ".", ".", ".", "6"},
		{".", ".", ".", ".", ".", "7", ".", ".", "."},
		{".", ".", ".", "5", ".", ".", ".", "7", "."},
	}))
}
